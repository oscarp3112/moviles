package ejercicio4;

import java.util.*;

public class Plano {

	public String cuadrante;
	public double coordenadaX;
	public double coordenadaY;

	public Plano() {}
	public Plano(String cuadrante, double x, double y) {
		this.cuadrante = cuadrante;
		this.coordenadaX = x;
		this.coordenadaY = y;
    }
}